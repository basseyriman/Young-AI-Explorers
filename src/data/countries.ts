export function mergeCountryLists<T extends { code: string; name: string; flag_emoji: string; is_featured?: boolean; explorer_count?: number }>(
  fromDb: T[] = []
): (T & { explorer_count: number })[] {
  const byCode = new Map<string, T & { explorer_count: number }>()

  for (const c of ALL_COUNTRIES) {
    byCode.set(c.code, { ...c, explorer_count: 0 } as T & { explorer_count: number })
  }

  for (const row of fromDb) {
    const existing = byCode.get(row.code)
    byCode.set(row.code, {
      ...(existing ?? { code: row.code, name: row.name, flag_emoji: row.flag_emoji, is_featured: row.is_featured ?? false, explorer_count: 0 }),
      ...row,
      explorer_count: row.explorer_count ?? existing?.explorer_count ?? 0,
    } as T & { explorer_count: number })
  }

  return Array.from(byCode.values()).sort((a, b) => {
    if (a.is_featured && !b.is_featured) return -1
    if (!a.is_featured && b.is_featured) return 1
    if (a.code === 'GLOBAL') return -1
    if (b.code === 'GLOBAL') return 1
    return a.name.localeCompare(b.name)
  })
}
